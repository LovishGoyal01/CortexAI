import pptxgen from "pptxgenjs"
const COLORS = {
  primary: "2563EB",
  secondary: "0F172A",
  text: "334155",
  light: "F8FAFC",
  border: "E2E8F0",
  white: "FFFFFF",
  muted: "64748B",
};

export const generatePpt = async (data) => {
    const ppt = new pptxgen()

    ppt.layout = "LAYOUT_WIDE"
    ppt.author = "CortexAI"
    ppt.title = data.title
    ppt.subject = data.title
    ppt.company = "CortexAI"

    ppt.theme = {
        headFontFace: "Aptos",
        bodyFontFace: "Aptos",
    }

    addCover(ppt, data)

    data?.slides?.forEach((s, i) => {
        addContentSlide(
            ppt,
            s.title,
            s.points,
            i + 1,
            data.slides.length
        )
    })

    addThankYou(ppt)
    return ppt
}


const addCover = (ppt, data) => {
      const slide = ppt.addSlide();

      slide.background = {
        color: COLORS.primary,
      };

      slide.addShape(ppt.ShapeType.rect, {
        x: 0,
        y: 0,
        w: 13.33,
        h: 0.18,
        fill: {
          color: "60A5FA",
        },
        line: {
          color: "60A5FA",
        },
      });

      slide.addText(data.title, {
        x: 0.7,
        y: 1.7,
        w: 12,
        h: 0.8,
        align: "center",
        color: COLORS.white,
        bold: true,
        fontSize: 28,
      })

      slide.addText(data.subtitle, {
        x: 1,
        y: 2.8,
        w: 11.5,
        h: 0.4,
        align: "center",
        color:"DBEAFE",
        fontSize: 15,
      }) 

      slide.addText("Generated by CortexAI", {
        x: 0,
        y: 6.9,
        w: 13.33,
        h: 0.2,
        align: "center",
        color:"BFDBFE",
        fontSize: 10,
      }) 
};

const addContentSlide = (ppt, title, points, page, total) => {
    const slide = ppt.addSlide();

      slide.background = {
        color: COLORS.white,
      };

      slide.addText(title, {
        x: 0.6,
        y: 0.35,
        w: 10,
        h: 0.4,
        color: COLORS.primary,
        bold: true,
        fontSize: 24,
      })

      slide.addShape(ppt.ShapeType.line, {
        x: 0.6,
        y: 0.85,
        w: 12,
        h: 0,
        line: {
          color: COLORS.border,
          width: 1.2,
        },
      });

      const visualPoints = points.slice(0, 6);

      visualPoints.forEach((point, i) => {
           const y = 1.2 + i * 0.75;
           slide.addShape(ppt.ShapeType.roundRect, {
             x: 0.7,
             y,
             w: 12,
             h: 0.58,
             fill: {
                color: i % 2 === 0 ? "F8FAFC" : "EFF6FF",
             },
             line: {
               color: COLORS.border,
             },
             radius: 0.08,
          });

          slide.addShape(ppt.ShapeType.ellipse, {
             x: 0.92,
             y: y + 0.18,
             w: 0.12,
             h: 0.12,
             fill: {
                color: COLORS.primary,
             },
             line: {
               color: COLORS.primary,
             },
             radius: 0.08,
          });

          slide.addText(point, {
            x: 1.2,
            y: y+0.08,
            w: 11,
            h: 0.35,
            color: COLORS.text,
            fit: "shrink",
            fontSize: 15,
         })
       });
       
       slide.addText(`${page}/${total}`, {
            x: 12.1,
            y: 6.9,
            w: 0.8,
            h: 0.2,
            color: COLORS.muted,
            align: "right",
            fontSize: 10,
       })

       slide.addText(`CortexAI`, {
            x: 0.6,
            y: 6.9,
            w: 2,
            h: 0.2,
            color: COLORS.muted,
            fontSize: 10,
         })

} 

const addThankYou = (ppt) => {
    const slide = ppt.addSlide();

      slide.background = {
        color: COLORS.secondary,
      };

      slide.addText("Thank You!", {
        x: 0,
        y: 2.2,
        w: 13.33,
        h: 0.8,
        color: COLORS.white,
        align: "center",
        bold: true,
        fontSize: 34,
      })   

      slide.addText("Generated by CortexAI", {
        x: 0,
        y: 6.9,
        w: 13.33,
        h: 0.2,
        color: "94A3B8",
        align: "center",
        fontSize: 10,
      })
}    